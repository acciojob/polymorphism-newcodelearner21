package com.driver;

public class Product {
    public static void  product(int x, int y) {
        System.out.println(x * y );

    }

    public static void  product(int x, int y, int z) {
        System.out.println(x * y * z);

    }

    public static void  product(double x, double y) {
        System.out.println(x * y);


    }

}
